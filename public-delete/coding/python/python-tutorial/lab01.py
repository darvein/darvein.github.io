# Numbers
print('------------------')
print(2**2)
print((50 - 5*6)/4)
print(17 / 3)   # float division
print(17 // 3)  # discards the fractional part
print(17 % 3)   # division remainder

# Use of variables
print('------------------')
width = 20
height = 5 * 9
print(width * height)

# Use of strings
print('------------------')
text = "Hello there!\nBye."
print(text + "...")
print(text[0:5])

# Lists
print('------------------')
numbers = [1, 2, 3, 4, 5]
numbers.append(6)
print("This is the array: {} of size {}".format(numbers, len(numbers)))
